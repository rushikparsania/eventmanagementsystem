  $(function () {
  	if ( ! $.fn.DataTable.isDataTable( '#example1' ) ) {
  		table = $("#example1").DataTable({
	      "responsive": true, 
	      "lengthChange": false, 
	      "autoWidth": false,
	      "bFilter": false,
	      //"buttons": ["copy", "csv", "excel", "pdf", "print", "colvis"]
	      "ajax": {
	      	"url":"index.php/eventsmanagement/list",
	      	"type":"post",
	      	"data":function(){}
	      },
	      columns: [
	      	{ data:"id", orderable:false, name:"id"},
	      	{ data:"title", orderable:false, name:"title"},
	      	{ data:"dates", orderable:false, name:"dates"},
	      	{ data:"recurrence_duration", orderable:false, name:"recurrence_duration"},
	      	{ data:"action", orderable:false, name:"action"}
	      ]
	    });//.buttons().container().appendTo('#example1_wrapper .col-md-6:eq(0)');
  	} else {
  		table.ajax.reload();
  	}

    $(document).on('click','.deletebtn', function() {
        var id = $(this).closest('tr').attr('id');
        
        $.ajax({
            url: 'index.php/eventsmanagement/deleteevent',
            type: "post",
            data: {id:id},
            success: function(data)
            {
                var result = $.parseJSON(data);
                if(result.status == 'success') {
                    table.ajax.reload();
                }
                alert(result.msg);
            }
        });
    });

    $(document).on('click','.viewdetails', function() {
        var id = $(this).closest('tr').attr('id');
        
        $.ajax({
            url: 'index.php/eventsmanagement/viewdetails',
            type: "post",
            data: {id:id},
            success: function(data)
            {
                var result = $.parseJSON(data);
                $('#exampleModalCenter .modal-body').html(result.data);
                $('#exampleModalCenter').modal("show");
            }
        });
    });
  });
</script>
